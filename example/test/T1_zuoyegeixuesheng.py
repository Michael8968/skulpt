import turtle,random
turtle.mode("logo")
turtle.shape("turtle")
turtle.colormode(255)
turtle.tracer(False)
turtle.pensize(3)
turtle.pencolor('red')
turtle.fillcolor(249,177,191)
turtle.begin_fill()
turtle.seth(-45)
turtle.circle(50,180)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.circle(50,180)
turtle.end_fill()
turtle.tracer(True)
