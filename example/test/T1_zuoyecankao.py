import turtle,random
turtle.mode("logo")
turtle.shape("turtle")
turtle.colormode(255)
turtle.tracer(False)
turtle.pensize(3)
turtle.pencolor('red')
turtle.fillcolor(249,177,191)
turtle.begin_fill()
turtle.seth(-45)
turtle.circle(50,180)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.circle(50,180)
turtle.end_fill()
turtle.tracer(True)

###练习啊开始
#数字1
turtle.pu()
turtle.goto(-20,-26)
turtle.seth(180)
turtle.pencolor('white')
turtle.pd()
turtle.forward(50)
#点
turtle.pu()
turtle.goto(0,-76)
turtle.seth(180)
turtle.pencolor('white')
turtle.pd()
turtle.dot(10)
#数字7
turtle.pu()
turtle.goto(20,-26)
turtle.seth(90)
turtle.pencolor('white')
turtle.pd()
turtle.forward(30)
turtle.right(90)
turtle.forward(50)

#####练习结束
turtle.done()
