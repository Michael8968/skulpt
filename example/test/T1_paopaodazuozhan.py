import turtle, random
turtle.mode("logo")
turtle.shape("turtle")
turtle.fillcolor("green")
turtle.pensize(5)
turtle.speed(1)


def move(x, y):
	turtle.pu()
	turtle.goto(x, y)
	turtle.pd()


def arrow(size):
	turtle.left(135)
	turtle.forward(size)
	turtle.forward(-1*size)
	turtle.left(90)
	turtle.forward(size)


